function editButton(btn, num, option) {
  btn.innerHTML =
    option + ")" + quizData.Question[num].options[option.charCodeAt(0) - 97];
  btn.class = "op_class";
  btn.onclick = () => {
    checkAns(option, num);
  };
  return btn;
}
